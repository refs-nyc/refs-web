  Yet most apps meant to “connect” us do all the work on our behalf: they gather huge networks of people then lock us into scrolling them, where they can serve us ads along the way, or <a class="underline" href="https://www.tiktok.com/@blondebutterpasta/video/7420940682367454470?lang=en">charge us to talk to the people they think we’d most like to meet</a>.
  
  But what good is a network we can’t navigate? What good is connection if it means thrusting us into a crowded room, forcing us to post just to be seen, to scroll endlessly just to discover? Real connection means being able to raise a hand, signal whatever we’d like to be found for, or what we’re seeking, and find each other without being sucked into an endless scroll. For friends, collaborators, dates– it should just work.
  
  This is exactly what we’re building with Refs– a <span class="font-bold">phonebook for the internet</span>. We think of it as an open network of mini personal websites, all searchable to each other. Anyone can opt-in to being found for whatever they like, and run searches, simple or complex, to find each other. Moms looking for a new parents group. Designers looking for cofounders. Our friend Paul looking for a girl who likes rock climbing, video games, and Icelandic food— nobody should have to squint at forums and comment sections, or swipe through thousands of profiles, to find the people they share life with. We deserve a starting point for great encounters. We hope you’ll find it useful.<br><br>
  
  
